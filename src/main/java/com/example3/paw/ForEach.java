package com.example3.paw;

public class ForEach {
    public static void main(String[] args) {
        String[] array = {
            "Rania", "Gisela", "Pramesty",
            "Progammer", "Zaman", "Now"
        };

        for (var value : array) {
            System.out.println(value);
        }
    }
    
}
